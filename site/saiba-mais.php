<div align="center"><h3>PNL</h3></div>
<br>
<div align="center"> <img class="img-responsive" src="img/pnl.jpg" width="500"></div>

<br>
<div class="saiba saibacell">
Estudo da estrutura da experiência subjetiva do ser humano. Também podemos definir a PNL como uma metodologia que foca no</p> alcance da excelência, ou seja, auxilia uma pessoa a atingir os resultados que deseja em sua vida, criando modelos a partir </p>de atitudes de sucesso.</p>
PNL nos ensina a pensar sobre soluções e resultados. É um modelo único e atualizado de desenvolvimento comportamental. As </p>mudanças comportamentais ocorrem de forma efetiva, pois a PNL foca na somatória do desenvolvimento de capacidades (COMO FAZER)</p> juntamente com a motivação para aplicar (POR QUE FAZER).</p>
A PNL funciona na construção de soluções, com foco no atingimento de metas e objetivos.</p>
</div>

