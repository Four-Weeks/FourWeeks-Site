<div align="center"><h3> Aprendizagem Natural de ensino</h3></div>
<br>
<div align="center"> <img class="img-responsive" src="img/jovem_aprendendo.jpg" width="500"></div>
</br>

<div class="saiba saibacell">
O método é inspirado nos princípios naturalísticos da aprendizagem de línguas, ou seja, os alunos aprendem de forma </p>espontânea, seguindo uma ordem natural de aprendizagem. A ênfase é dada à exposição à língua (input) mais do que à prática,</p> à preparação emocional do aprendiz e a um longo período de ouvir a língua antes de falar.</p>
O método natural considera a comunicação como a função primordial da língua e o significado mais importante do que a forma.</p>
Esse método é como você aprendeu a falar o português, você OUVIU, ENTENDEU, DEPOIS FALOU E BEM DEPOIS ESCREVEU!!!</p>

