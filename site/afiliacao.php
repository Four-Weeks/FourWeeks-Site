        function Click_Botao() {
            var formData = new FormData();
            formData.append('nome', $('#nome').val());
            formData.append('email', $('#email').val());
            formData.append('texto', $('#texto').val());
            formData.append('venda1', $('#venda1').val());
            formData.append('venda2', $('#venda2').val());
            formData.append('venda3', $('#venda3').val());
            formData.append('veio_por', "<?php echo $veio_por;?>"); 
            $.ajax({
            type: 'POST',
            dataType: 'html',
            url: "../API/afiliacao_cadastro_auto.php",
            data: formData,
            contentType: false,
            processData: false,
            success: function (data){
            
            if (data.includes("OK:")){
            $('#FORM_afiliacao').html(data);
            $('#R_FORM').html("");
            }else{
            $('#R_FORM').html(data);
            }
            
            }
            });
        }
